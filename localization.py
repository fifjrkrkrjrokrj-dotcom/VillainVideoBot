def get_text(key, lang="en", *args):
    """Retrieve localized message by key and format it if args are passed."""
    lang_dict = MESSAGES.get(lang, MESSAGES["en"])
    template = lang_dict.get(key, MESSAGES["en"].get(key, f"[{key}]"))
    if args:
        try:
            return template.format(*args)
        except Exception:
            return template
    return template
